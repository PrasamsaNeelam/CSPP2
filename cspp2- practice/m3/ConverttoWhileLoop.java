class ConverttoWhileLoop
{
    /**
     * { main function }.
     *
     * @param      args  The arguments
     */
    public static void main(String[] args)
    {
        int i = 2;
        while (i <= 10)
        {
            System.out.println(i);
            i += 2;
        }
        System.out.println("GoodBye!");
    }
}